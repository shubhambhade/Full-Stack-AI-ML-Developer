class Math:

    @staticmethod
    def add(a,b):
        print("Sum  : ", a+b)

    @staticmethod
    def product(a,b):
        print("Multiplicatio : ", a*b)
        
    @staticmethod
    def average(a,b):
        print("Average : ", (a+b)/2)

Math.add(10,20)
Math.product(10,20)
Math.average(10,20)